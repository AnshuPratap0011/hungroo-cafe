<?php

/* =========================================================
DATABASE CONFIG
========================================================= */

$host =
"localhost";

$username =
"root";

$password =
"";

$database =
"boba_hungroo";

/* =========================================================
CONNECTION
========================================================= */

$conn = mysqli_connect(

    $host,

    $username,

    $password,

    $database

);

/* =========================================================
CHECK CONNECTION
========================================================= */

if(!$conn){

    die(

        "Database Connection Failed : "

        . mysqli_connect_error()

    );

}

/* =========================================================
TIMEZONE
========================================================= */

date_default_timezone_set(
"Asia/Kolkata"
);

/* =========================================================
UTF8
========================================================= */

mysqli_set_charset(
$conn,
"utf8mb4"
);

?>