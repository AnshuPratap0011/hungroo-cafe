<?php

/* =========================================================
FILE: logout.php
SECURE LOGOUT SYSTEM
========================================================= */

include "config/config.php";

/* =========================================================
REMOVE ALL SESSION
========================================================= */

$_SESSION = [];

/* =========================================================
DESTROY SESSION
========================================================= */

session_destroy();

/* =========================================================
REMOVE COOKIE
OPTIONAL
========================================================= */

if(

    isset($_COOKIE[session_name()])

){

    setcookie(

        session_name(),

        '',

        time() - 3600,

        '/'

    );

}

/* =========================================================
REDIRECT
========================================================= */

header("Location: login.php");
exit;

?>